DROP DATABASE IF EXISTS beverages; 

CREATE DATABASE beverages; 

USE beverages; 

CREATE TABLE Sodas (
	SodaID INT PRIMARY KEY AUTO_INCREMENT, 
    SodaName VARCHAR(50) NOT NULL, 
    SodaVolume VARCHAR(50) NOT NULL
);

CREATE TABLE Teas (
	TeaID INT PRIMARY KEY AUTO_INCREMENT, 
    SodaID INT NOT NULL, 
    TeaBrand VARCHAR(50) NOT NULL, 
    TeaName VARCHAR(50) NOT NULL, 
    TeaTag BOOLEAN
);

ALTER TABLE Teas
ADD CONSTRAINT FK_Teas_Sodas
FOREIGN KEY (SodaID)
REFERENCES Sodas(SodaID);

CREATE TABLE SodaTeas (
	SodaTeaID INT PRIMARY KEY AUTO_INCREMENT, 
    SodaTeaName VARCHAR(255) NOT NULL,
    Rating INT NOT NULL, 
    Notes VARCHAR(500) NOT NULL
);
